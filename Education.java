
public class Education{
  int id;
  int category;
  String name;
  String detail;
  String image;

  public Education(){}

  public Education(int id,int category,String name,String detail,String image){
    this.id=id;
    this.category=category;
    this.name=name;
    this.detail=detail;
    this.image=image;
  }
}